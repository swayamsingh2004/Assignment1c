#include <stdio.h>
int main(){
    float a,b;
    printf("Enter the value of a:\n");
    scanf("%f", &a);
    printf("Enter the value of b:\n");
    scanf("%f", &b);

    printf("Sum of both the numbers is %f \n", a+b);
    printf("Subtraction of both the numbers is %f \n", a-b);
    printf("multiplication of both the numbers is %f \n", a*b);

    return 0;

}